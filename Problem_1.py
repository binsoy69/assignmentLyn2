print('** A program that reads in two integers and then outputs their sum, difference, and product **') # title

num1 = int(input('Enter the 1st integer: ')) 
num2 = int(input('Enter the 2nd integer: '))  

sum = num1 + num2  
difference = num1 - num2
product = num1 * num2

print('The sum of the two integers is ', sum) 
print('The difference of the two integers is ', difference) 
print('The product of the two integers is ', product)


